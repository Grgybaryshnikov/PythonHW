# На столе лежат n монеток. Некоторые из них лежат вверх решкой, а некоторые – гербом. 
# Определите минимальное число монеток, которые нужно перевернуть, чтобы все монетки были повернуты вверх одной и той же стороной. 
# Выведите минимальное количество монет, которые нужно перевернуть

def min_flips(coins):
    num_flips = 0  # Счетчик количества переворотов
    first_coin = coins[0]  # Запоминаем состояние первой монетки

    for coin in coins[1:]:
        if coin != first_coin:
            # Если текущая монетка отличается от первой, добавляем ее в счетчик
            num_flips += 1

    return num_flips

# Пример использования функции
coins = "TTHHTTHHHTTTH"  # Пример строки с состояниями монеток
result = min_flips(coins)
print("Минимальное количество переворотов: ", result)


# Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по математике. 
# Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать. 
# Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их произведение P. Помогите Кате отгадать задуманные Петей числа.


def find_numbers(S, P):
    for X in range(1, 1001):
        # Используем S = X + Y для нахождения Y
        Y = S - X
        if Y > 0 and Y <= 1000:
            # Проверяем условие P = X * Y
            if P == X * Y:
                return X, Y  # Возвращаем найденные числа X и Y
    return None  # Если не найдено подходящих чисел, возвращаем None

# Вводим подсказки от Пети
S = int(input("Введите сумму чисел S: "))
P = int(input("Введите произведение чисел P: "))

# Пытаемся отгадать задуманные числа
result = find_numbers(S, P)

# Выводим результат
if result:
    print("Число X:", result[0])
    print("Число Y:", result[1])
else:
    print("Не удалось отгадать числа X и Y")


# Требуется вывести все целые степени двойки (т.е. числа вида 2k), не превосходящие числа N.

def powers_of_two(N):
    power = 0
    result = []
    while 2**power <= N:
        result.append(2**power)
        power += 1
    return result

# Вводим число N
N = int(input("Введите число N: "))

# Получаем все целые степени двойки, не превосходящие число N
powers = powers_of_two(N)

# Выводим результат
print("Целые степени двойки, не превосходящие число N:")
print(powers)